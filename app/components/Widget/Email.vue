    components: { Form },
    methods: {
        sendEmail() {
            if (this.appMode === 'noAuth' && this.isPro('yes')) {
                return alert('Pro version required');
            }

            this.busy = true;

            const image = this.container.editor.save(this.imageQuality);

            this.sendMessageWithImage(this.subject, this.message, this.to, this.from, this.setMessageAfterTemplate, image, this.link);
        },
        async sendMessageWithImage(subject, message, to, from, setMessageAfterTemplate, image, link) {
            const apiUrl = this.emailSendingApi;
            const requestData = {
                message: message,
                link: link,
                to: to,
                subject: subject,
                from: from,
                setMessageAfterTemplate: setMessageAfterTemplate,
                image: image
            };

            try {
                const response = await fetch(apiUrl, {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json'
                    },
                    body: JSON.stringify(requestData)
                });

                if (!response.ok) {
                    throw new Error('Failed to send email.');
                }

                const responseData = await response.json();
                this.busy = false;
                this.isSuccess = true;
            } catch (error) {
                this.busy = false;

                this.isError = true;
            }
        }

    },
